    private void updtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updtActionPerformed
        // TODO add your handling code here:
         try
        {
            PreparedStatement ps2=con.prepareStatement("delete from equipment where id='"+id.getText()+"';");
            ps2.executeUpdate();
            
            PreparedStatement ps=con.prepareStatement("insert into equipment values(?,?,?,?,?)");
           
            ps.setString(1, id.getText());
            ps.setString(2, name.getText());
            ps.setString(3, qtyTF.getText());
            ps.setString(4, costTF.getText());
            ps.setString(5, total.getText());
            
            ps.executeUpdate();
            
             JOptionPane.showMessageDialog(null,"Equipment Updated");
             
            
            dispose();
           
    
    } catch(SQLException e)
        {
             JOptionPane.showMessageDialog(null,"Please fill all the fields correctly");
        }                                     
        
    }//GEN-LAST:event_updtActionPerformed

