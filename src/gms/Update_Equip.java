    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try
        {
            PreparedStatement ps=con.prepareStatement("select * from equipment where id='"+id.getText()+"';");      
            ResultSet rs=ps.executeQuery();
            
            if(!rs.next())
                JOptionPane.showMessageDialog(null,"Record Not Found");
            else
            {
                //regid.setText(rs.getString(2));
                id.setEnabled(false);
                updt.setEnabled(true);
                del.setEnabled(true);
    
                name.setText(rs.getString(2));
                qtyTF.setText(rs.getString(3));
                costTF.setText(rs.getString(4));
                total.setText(rs.getString(5));
               
            }         
        } catch(SQLException e)
        {
             JOptionPane.showMessageDialog(null,e);
        }  

    }//GEN-LAST:event_jButton2ActionPerformed

