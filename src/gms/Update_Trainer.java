    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        
        try
        {
            PreparedStatement ps=con.prepareStatement("select * from trainer where reg_id='"+regid.getText()+"';");      
            ResultSet rs=ps.executeQuery();
            
            if(!rs.next())
                JOptionPane.showMessageDialog(null,"Record Not Found");
            else
            {
                regid.setEnabled(false);
                getregid.setVisible(false);
                msg.setVisible(false);
                upl.setEnabled(true);
                dell.setEnabled(true);
                name.setText(rs.getString(2));
                age.setText(rs.getString(3));
                gender.setSelectedItem(rs.getString(4));
                pkg.setText(rs.getString(5));
                cont.setText(rs.getString(6));
                mail.setText(rs.getString(7));
                ((JTextField)dateTF.getDateEditor().getUiComponent()).setText(rs.getString(8));
//              
            }
    
                      
                
            
        } catch(SQLException e)
        {
             JOptionPane.showMessageDialog(null,e);
        }  

    }//GEN-LAST:event_searchActionPerformed

