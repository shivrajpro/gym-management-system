    private void uplActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uplActionPerformed
        // TODO add your handling code here:
        try
        {
            PreparedStatement ps2=con.prepareStatement("delete from trainer where reg_id='"+regid.getText()+"';");
            ps2.executeUpdate();
            
            
            PreparedStatement ps=con.prepareStatement("insert into trainer values (?,?,?,?,?,?,?,?);");
            ps.setString(1, regid.getText());
            ps.setString(2, name.getText());
            ps.setString(3, age.getText());
            ps.setString(4, (String) gender.getSelectedItem());
            ps.setString(5, pkg.getText());
            ps.setString(6, cont.getText());
            ps.setString(7, mail.getText());
            ps.setString(8, ((JTextField)dateTF.getDateEditor().getUiComponent()).getText());
            
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Trainer Updated Successfully!");
    
    } catch(SQLException e)
        {
             JOptionPane.showMessageDialog(null,"Please fill all the fields correctly");
        }                                     
    }//GEN-LAST:event_uplActionPerformed

